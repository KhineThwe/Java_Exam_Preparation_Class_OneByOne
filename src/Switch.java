
public class Switch {

	public static void main(String[] args) {
		int day = 11;
		switch(day) {
		case 1:
			//operation
			System.out.println("Monday");
			break;
		case 2:
			System.out.println("Tuesday");
			break;
		case 3:
			System.out.println("Wednesday");
			break;
		default:
			System.out.println("Invalid Day");
		}

	}

}
